<b>Main campus-TCS:</b>
<br>

Coming to the technical batches, they divide a batch of trainees in to LG’s(Learning groups) based on their respective domains. The domains are Java,Oracle,SAP,.NET, there are two places for training one is . On very first day you have to report at People Park Campus .
<br>
<b>1.</b>HR will verify our documents
<br>
<b>2.</b>IRA1 & IRA2 exams are conducted
<br>
<b>3.</b>They will take photos for our Smart cards and various bank professionals will be there to open your salary account.
<br>
<b>4.</b>Please carry passport size photographs with you least 10, and don’t carry any media like pen drives, hard disks with you they are strictly prohibited.
<br>
<b>5.</b>They will divide us according to our domains and ask us to report the next day at our respective places.
<br>
<b>6.</b>The ILP centre is located at the 5th floor,NedFi house, G.S Road, Ganeshguri.
<br>

 
<b>Training:</b>
<br>
<b>(i) Technical:</b>

<b>1.</b>For Java the training is hard, but one can manage if he/she keep some effort. Believe me you have to attend many tests like PS1,PS2,IM,Servlets,Struts,Hibernate and a project. But don’t take tension everything will be fine
<br>

<b>2.</b>.Net is also same as java but some what better
<br>
<b>3.</b>SAP is like heaven, no exams nothing only Presentations. One who get SAP can enjoy a lot. But SAP Abab will have exams.
<br>
<b>4.</b>Oracle is medium, if you keep a little effort you can clear it.
<br>
<b>(ii) Business Skills:</b>
<br>
The main thing which is common for every batch is “BUSINESS SKILLS” most important and take care while doing this on the very 2nd you may asked to write on a topic and next Bizz lead will ask to give a talk on a topic in front of a camera. Guys take it serious, if you fail to clear this you have to face “Remedial” which may lead to “LAP” the extension of training period for 20 days. So please clear at first attempt… Bizz Classes are very important.
<br>
<b>Remedial & LAP (Applicable for both Technical & BIZZ):</b>
<br>

Remedial is given to a trainee who did not do well in the technical and bizz. For technical if you are in remedial you have to give the exam again in which you are fail, sometimes you have to give VIVA also.

<br>
One will get moved to LAP if he/she fails to clear the remedial. LAP means an extra period of training give to associates who are in LAP, that means after your training period (3 or 2 months) you have stay there for extra 20 days and they will not give you accommodation.
In technical skills there is a less chance of getting in to LAP most one can clear in Remedial itself. But in Bizz skills LAP is common like they will give it to at least one person in a LG. So be careful with Bizz Skills.
